// Arithmetic Operator
//[+, -, *, /, %, **, ++, --]

const x = 5;
const y = 10;

// Addition
const sum = x + y;
console.log(sum);

// Subtraction
const sub = x - y;
console.log(sub);

// Multiplication
const multi = x * y;
console.log(multi);

// division
const divide = x / y;
console.log(divide);

// modulus
const module1 = x % y;
console.log(module1);

// Exponentiation
const expo = 5 ** 2;
console.log(expo)

// Increment
let Increment_new = 5;
let Increment = ++Increment_new;
console.log(Increment);

// Decrement(--)
let Decrement = --Increment_new;
console.log(Decrement);

// Concatenation
const load = "Tejas" + 7
console.log(load);

// string operation with divide
let str_div = "Tejas54212" / 5;
console.log(str_div); // O/P : NaN = Not a Number

